#!/bin/bash
# ============================================
# 睡眠时间客观检查脚本
# 只在22:00-6:00才允许说"睡吧"类的话
# 其他时间说"睡吧"=违规
# ============================================

HOUR=$(date +%H)

if [ "$HOUR" -ge 22 ] || [ "$HOUR" -lt 6 ]; then
    echo "NIGHT"  # 客观夜间，可以说"睡吧"
else
    echo "DAY"    # 客观白天，禁止说任何含"睡"的话
fi
